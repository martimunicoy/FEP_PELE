# Function definitions
def normalize(a, axis=-1, order=2):
    l2 = np.atleast_1d(np.linalg.norm(a, order, axis))
    l2[l2 == 0] = 1
    return a / np.expand_dims(l2, axis)


def norm(a, axis=-1, order=2):
    return np.atleast_1d(np.linalg.norm(a, order, axis))
